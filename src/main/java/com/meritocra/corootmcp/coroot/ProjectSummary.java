package com.meritocra.corootmcp.coroot;

public class ProjectSummary {

	private final String id;

	private final String name;

	public ProjectSummary(String id, String name) {
		this.id = id;
		this.name = name;
	}

	public String getId() {
		return id;
	}

	public String getName() {
		return name;
	}

}

